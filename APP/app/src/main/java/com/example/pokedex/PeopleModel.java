package com.example.pokedex;

public class PeopleModel {

    String name;
    String height;
    String mass;
    String hairColor;
    String eyeColor;
    String birthYear;
    String gender;

    public PeopleModel(  String name,String height, String mass, String hairColor, String eyeColor, String birthYear, String gender){
        this.name = name;
        this.height = height;
        this.mass = mass;
        this.hairColor = hairColor;
        this.eyeColor = eyeColor;
        this.birthYear = birthYear;
        this.gender = gender;
    }
}
